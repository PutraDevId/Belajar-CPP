#include <iostream>

int main (){

    //preincrement dan postincrenment
    int a = 5;
    int b = 5;

    //postincrenment
    std::cout << a << std::endl;
    // a = a + 1
    a++;
    std::cout << a << std::endl << std::endl;
    

    //preincrenment
    std::cout << b << std::endl;
    ++b;
    std::cout << b << std::endl;




    return 0;
}